
CREATE TABLE HOBBY_CODE
(
  HOOBBY_NAMS   VARCHAR NULL     COMMENT '취미명',
  HOBBY_CODE_ID VARCHAR NOT NULL COMMENT '유니크 ID',
  PRIMARY KEY (HOBBY_CODE_ID)
) COMMENT '취미코드';

CREATE TABLE MEMBERS
(
  NAME      VARCHAR   NULL     COMMENT '이름',
  AGE       INESTRING NULL     COMMENT '나이',
  GENDER    VARCHAR   NULL     COMMENT '성별',
  ADDRESS   VARCHAR   NULL     COMMENT '주소',
  TELEPHONE VARCHAR   NULL     COMMENT '전화번호',
  UNIQE_ID  VARCHAR   NOT NULL COMMENT '유니크ID',
  PRIMARY KEY (UNIQE_ID)
) COMMENT '회원정보';

CREATE TABLE MEMBERS_HOBBY
(
  HOBBY_CODE_ID VARCHAR NOT NULL COMMENT '유니크 ID',
  HOBBY_CODE_ID VARCHAR NOT NULL COMMENT '유니크 ID'
) COMMENT '회원_ 취미';

CREATE TABLE MEMBERS_LOGIN
(
  EMAIL         VARCHAR NULL     COMMENT '이메일',
  MEMBERS_LOGIN VARCHAR NOT NULL COMMENT '회원 로그인ID',
  UNIQE_ID      VARCHAR NULL     COMMENT '유니크ID',
  PRIMARY KEY (MEMBERS_LOGIN)
) COMMENT '회원 로그인';

ALTER TABLE MEMBERS_LOGIN
  ADD CONSTRAINT FK_MEMBERS_TO_MEMBERS_LOGIN
    FOREIGN KEY (UNIQE_ID)
    REFERENCES MEMBERS (UNIQE_ID);

ALTER TABLE MEMBERS_HOBBY
  ADD CONSTRAINT FK_HOBBY_CODE_TO_MEMBERS_HOBBY
    FOREIGN KEY (HOBBY_CODE_ID)
    REFERENCES HOBBY_CODE (HOBBY_CODE_ID);
