
CREATE TABLE CHOLCE
(
  CHOICE_ID VARCHAR(50) NOT NULL COMMENT '설문 답항 코드',
  CHOICE    VARCHAR(50) NOT NULL COMMENT '설문 답항',
  PRIMARY KEY (CHOICE_ID)
) COMMENT '설문 답항';

CREATE TABLE QUESTION_CHOICE
(
  QUESYIONS_ID VARCHAR(50) NOT NULL COMMENT '설문 문항 코드',
  CHOICE_ID    VARCHAR(50) NOT NULL COMMENT '설문 답항 코드'
) COMMENT '문답';

CREATE TABLE QUESTIONS
(
  QUESTIONS    VARCHAR(50) NOT NULL COMMENT '설문문항',
  QUESYIONS_ID VARCHAR(50) NOT NULL COMMENT '설문 문항 코드',
  PRIMARY KEY (QUESYIONS_ID)
) COMMENT '설문 문항';

CREATE TABLE RESPONDENTS
(
  RESPONDENTS    VARCHAR(50) NOT NULL COMMENT '설문자',
  RESPONDENTS_ID VARCHAR(50) NOT NULL COMMENT '설문자 _ID',
  RESPONDENTS_PW VARCHAR(50) NOT NULL COMMENT '설문자 _PW',
  PRIMARY KEY (RESPONDENTS_PW)
) COMMENT '설문자 ';

CREATE TABLE STATISTICS
(
  STATISTICS_ID  VARCHAR(50) NOT NULL COMMENT '설문자 통계 코드',
  RESPONDENTS_ID VARCHAR(50) NOT NULL COMMENT '설문자 코드',
  CHOICE_ID      VARCHAR(50) NOT NULL COMMENT '설문 답항 코드',
  RESPONDENTS_PW VARCHAR(50) NOT NULL COMMENT '설문자 _PW',
  QUESYIONS_ID   VARCHAR(50) NOT NULL COMMENT '설문 문항 코드',
  PRIMARY KEY (STATISTICS_ID)
) COMMENT '설문 통계';

